let selector = document.getElementById('language-selector');

// استرجاع اللغة المخزنة في localStorage عند تحميل الصفحة
document.addEventListener('DOMContentLoaded', (event) => {
    let storedLanguage = localStorage.getItem('selectedLanguage');
    if (storedLanguage) {
        selector.value = storedLanguage;
        updateLanguage();
    } else {
        updateLanguage(); // لضبط اللغة الافتراضية عند أول تحميل
    }
});











selector.onchange = updateLanguage;
function updateLanguage(){
  let language = selector.options[selector.selectedIndex].value;
  let nodes = document.querySelectorAll('[data-lang]');

    let i = nodes.length;

    while(i--) {
        let key = nodes[i].getAttribute('data-lang');
        nodes[i].innerHTML = lang[language][key];
    }


    if(language === "en"){
        document.dir = "ltr";
    }else{
        document.dir = 'rtl';
    }
    
    

    localStorage.setItem('selectedLanguage', language);
}

